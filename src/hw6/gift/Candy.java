package hw6.gift;

public class Candy {
	private String name;
	private int weight;
	private double sugar;

	public Candy(String name, int weight, double sugar) {
		this.name = name;
		this.weight = weight;
		this.sugar = sugar;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return this.name;
	}

	public void setWeight(String name) {
		this.weight = weight;
	}

	public int getWeight() {
		return this.weight;
	}

	public void setSugar(String name) {
		this.weight = weight;
	}

	public double getSugar() {
		return this.sugar;
	}
}
